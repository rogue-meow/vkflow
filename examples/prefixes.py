# =============================================
# 7. Переопределение префикса для команды
# =============================================


# Глобальный префикс - /, !
# Но для этой команды используется только ">"
@app.command("секрет", prefixes=[">"])
async def secret(ctx: commands.Context):
    """Команда с собственным префиксом"""
    return "Это секретная команда!"


# >секрет  - сработает
# !секрет  - НЕ сработает


# =============================================
# 8. ctx.clean_prefix и ctx.invoked_with
# =============================================


@app.command("помощь", aliases=["help", "хелп"])
async def help_cmd(ctx: commands.Context):
    """Демонстрация clean_prefix и invoked_with"""
    # ctx.prefix       - фактически использованный префикс (включая упоминание)
    # ctx.clean_prefix - то же, но для упоминаний возвращает ""
    # ctx.invoked_with - имя/алиас, которым вызвали команду

    prefix = ctx.clean_prefix  # "!" или "/" или "" (если упоминание)
    name = ctx.invoked_with  # "помощь", "help" или "хелп"

    return (
        f"Вы вызвали: {prefix}{name}\n"
        f"Доступные команды:\n"
        f"  {prefix}ping - проверка бота\n"
        f"  {prefix}помощь - эта справка"
    )


# --- Запуск бота ---
app.run("$VK_TOKEN")
