"""
Примеры работы с префиксами.

Демонстрирует:
- Несколько префиксов
- Без префикса
- Динамические префиксы (функция)
- Асинхронные префиксы (из БД)
- when_mentioned / when_mentioned_or
- Переопределение префикса для конкретной команды
- ctx.clean_prefix и ctx.invoked_with
"""

import vkflow

from vkflow import commands


# =============================================
# 1. Несколько префиксов
# =============================================

app = vkflow.App(prefixes=["/", "!", "."])


# Сработает на: /ping, !ping, .ping
@app.command()
async def ping(ctx: commands.Context):
    """Простая команда с несколькими префиксами"""
    return "Pong!"


# =============================================
# 2. Без префикса
